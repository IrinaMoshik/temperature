def feet_to_meters(feet):
    """
    Convert feet to meters using the formula: meters = feet * 0.3048
    """
    meters = feet * 0.3048
    return meters

def meters_to_feet(meters):
    """
    Convert meters to feet using the formula: feet = meters / 0.3048
    """
    feet = round(meters / 0.3048,1)
    return feet